/*
### Task-1  
Take four parameters. Multiply the four numbers and then return the result 
 */

function multiplyNums(num1, num2, num3, num4) {
  return num1 * num2 * num3 * num4;
}

let num1 = 5;
let num2 = 2;
let num3 = 3;
let num4 = 10;
let result = multiplyNums(num1, num2, num3, num4);
console.log(result);
